package com.dreamteam.powerofwar.game.object;

/**
 * Статические объекты на карте.
 */
public enum StaticGameObjectType {
    /**
     * Месторождение. Можно построить шахту для добычи ресурсов.
     */
    DEPOSIT,

    /**
     * Деревья. Может использоваться в качестве ресурсов.
     */
    TREE,

    /**
     * Строения. Построено человеком.
     */
    BUILDING
}
